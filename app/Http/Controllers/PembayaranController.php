    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request, $pengirim, $tujuan, $pesanan)
    {

        $product_image = $request->file('gambar');
        $gambar = $product_image->getClientOriginalName();
        $tujuan_upload = './assets/images';
        $product_image->move($tujuan_upload, $gambar);

        Pembayaran::create([
            'pengirim_id' => $pengirim,
            'penerima_id' => $tujuan,
            'pesanan_id' => $pesanan,
            'bukti_pembayaran' => $gambar
        ]);

        Pesanan::where('id', $pesanan)->update([
            'status' => 'diproses'
        ]);

        return redirect('/pesanan_user');
    }

